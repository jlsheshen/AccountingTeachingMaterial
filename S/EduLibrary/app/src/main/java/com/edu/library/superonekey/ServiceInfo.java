package com.edu.library.superonekey;

/**
 * 系统更新中，ip地址和端口号信息
 * 
 * @author lucher
 * 
 */
public class ServiceInfo {

	// ip地址
	private String ip;
	// 服务器端口号
	private int port;

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	public int getPort() {
		return port;
	}

	public void setPort(int port) {
		this.port = port;
	}

}
